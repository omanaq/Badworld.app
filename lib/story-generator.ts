import { generateInternalConflictStory } from "./story-content/internal-conflict"
import { generateRebellionStory } from "./story-content/rebellion"
import { generateExistentialBreakdownStory } from "./story-content/existential-breakdown"

export function generateStoryContent(id: number, title: string, image: string): string {
  // استخدام معرف القصة لإنشاء محتوى مختلف لكل قصة
  const seed = id % 10 // استخدام باقي القسمة على 10 لتحديد نوع القصة

  // قائمة بالمواضيع المختلفة للقصص
  const themes = [
    "الصراع النفسي الداخلي",
    "التمرد على الواقع",
    "الانكسار الوجودي",
    "السخرية السوداء",
    "التجارب النفسية المؤلمة",
    "البحث عن الذات",
    "الاغتراب والوحدة",
    "الصراع بين الماضي والحاضر",
    "الحنين إلى ما لا يمكن استعادته",
    "مواجهة الحقيقة المؤلمة",
  ]

  // اختيار موضوع القصة بناءً على المعرف
  const theme = themes[seed]

  // إنشاء محتوى القصة بناءً على الموضوع
  let content = ""

  switch (seed) {
    case 0:
      content = generateInternalConflictStory(title, image)
      break
    case 1:
      content = generateRebellionStory(title, image)
      break
    case 2:
      content = generateExistentialBreakdownStory(title, image)
      break
    case 3:
      content = generateDarkHumorStory(title, image)
      break
    case 4:
      content = generatePainfulExperienceStory(title, image)
      break
    case 5:
      content = generateSelfDiscoveryStory(title, image)
      break
    case 6:
      content = generateAlienationStory(title, image)
      break
    case 7:
      content = generatePastPresentConflictStory(title, image)
      break
    case 8:
      content = generateNostalgiaStory(title, image)
      break
    case 9:
      content = generateFacingTruthStory(title, image)
      break
    default:
      content = generateDefaultStory(title, image)
  }

  return content
}

