export function generateExistentialBreakdownStory(title: string, image: string): string {
  return `
    <div class="existential-story">
      <h1>${title}</h1>
      ${image ? `<img src="${image}" alt="${title}" class="story-image">` : ''}

      <p>انكسرت. ليس انكساراً جسدياً يمكن رؤيته، بل انكساراً روحياً، وجودياً، انكساراً في صميم كياني. كأن شيئاً ما داخلي قد تحطم، وأصبحت أجمع شظاياه المتناثرة، محاولاً إعادة تركيبها، لكن دون جدوى.</p>

      <p>بدأ الأمر ببساطة، بسؤال عابر: "ما معنى كل هذا؟" سؤال بسيط، لكنه فتح باباً لم أستطع إغلاقه. باباً يؤدي إلى متاهة من الأسئلة التي لا إجابة لها، من الشكوك التي لا يقين معها، من الفراغ الذي لا يمكن ملؤه.</p>

      <p>كل ما كنت أؤمن به، كل ما كنت أثق فيه، كل ما كنت أعتقد أنه حقيقي، بدأ يتلاشى، يذوب، يختفي. كأنني استيقظت فجأة لأجد نفسي في عالم غريب، لا أعرف قوانينه، لا أفهم لغته، لا أدرك معناه.</p>

      <p>حاولت التشبث بشيء ما، بأي شيء. بالعمل، بالعلاقات، بالهوايات. لكن كل شيء بدا فارغاً، بلا معنى، بلا قيمة. كأن الحياة فقدت ألوانها، وأصبحت مجرد ظلال رمادية، باهتة، باردة.</p>

      <p>في الليل، كنت أستلقي في سريري، أحدق في السقف، أستمع إلى دقات قلبي. دقات منتظمة، ميكانيكية، بلا روح. وأتساءل: "هل هذا كل شيء؟ هل هذه هي الحياة؟ مجرد دقات قلب تستمر حتى تتوقف؟"</p>

      <p>في النهار، كنت أتحرك كالآلة. أستيقظ، أرتدي ملابسي، أذهب إلى العمل، أعود إلى المنزل، أنام. روتين يتكرر يوماً بعد يوم، أسبوعاً بعد أسبوع، شهراً بعد شهر. بلا هدف، بلا معنى، بلا غاية.</p>

      <p>حاولت التحدث مع الآخرين عما أشعر به. لكنهم لم يفهموا. كيف يمكنهم أن يفهموا؟ كيف يمكن لأحد أن يفهم هذا الفراغ الذي يملأ روحي، هذا العدم الذي يسكن قلبي، هذا اللامعنى الذي يخيم على وجودي؟</p>

      <p>بعضهم اقترح علي الذهاب إلى طبيب نفسي. بعضهم نصحني بممارسة الرياضة. بعضهم أخبرني أن ما أشعر به هو مجرد مرحلة ستمر. لكنهم جميعاً لم يفهموا أن ما أعاني منه ليس مرضاً يمكن علاجه، بل هو إدراك لا يمكن التراجع عنه.</p>

      <p>في إحدى الليالي، وبينما كنت أحدق في السقف كالعادة، خطرت لي فكرة. فكرة بسيطة، لكنها غيرت كل شيء. فكرة أن الانكسار ليس نهاية، بل بداية. بداية لفهم جديد، لوعي جديد، لوجود جديد.</p>

      <p>بدأت أقرأ. أقرأ كل ما يمكنني العثور عليه عن الفلسفة الوجودية، عن العدمية، عن العبثية. كامو، سارتر، نيتشه، كيركيغارد. كلمات تتردد في ذهني، أفكار تتشكل في عقلي، فهم يتبلور في روحي.</p>

      <p>وببطء، بدأت أفهم. أفهم أن الانكسار الوجودي ليس مرضاً، بل هو الشفاء من مرض الوهم. وأن الشعور بالعبثية ليس ضعفاً، بل هو قوة الإدراك. وأن الإحساس باللامعنى ليس يأساً، بل هو بداية الحكمة.</p>

      <p>بدأت أرى العالم بعيون جديدة. عيون لا تبحث عن معنى في عالم بلا معنى، لا تسعى وراء هدف في وجود بلا هدف، لا تطلب إجابات لأسئلة لا إجابة لها. عيون ترى العالم كما هو، بكل عبثيته، بكل جماله، بكل بشاعته.</p>

      <p>وفي هذه الرؤية الجديدة، وجدت نوعاً من السلام. سلام يأتي من قبول العبث، من تقبل اللامعنى، من احتضان الفراغ. سلام لا يأتي من إيجاد إجابات، بل من التوقف عن طرح الأسئلة.</p>

      <p>الانكسار الوجودي لم يختفِ. لا يزال هناك، في أعماق روحي، في زوايا قلبي، في أركان عقلي. لكنه لم يعد يؤلمني كما كان. لم يعد يشل حركتي، يمنعني من العيش، يحرمني من الشعور.</p>

      <p>بدأت أعيش مع انكساري، أتعايش معه، أتقبله كجزء مني. جزء يذكرني دائماً بحقيقة الوجود، بعبثية الحياة، بفراغ الكون. جزء يمنعني من الوقوع في فخ الوهم، من الانجراف وراء الأكاذيب، من الخضوع للأوهام.</p>

      <p>وفي هذا التعايش، وجدت نوعاً من الحرية. حرية من قيود المعنى، من ضغوط الهدف، من أعباء الغاية. حرية تسمح لي بالعيش كما أريد، بالشعور كما أشاء، بالوجود كما أنا.</p>

      <p>الآن، عندما أستلقي في سريري في الليل، وأحدق في السقف، وأستمع إلى دقات قلبي، لا أشعر بالفراغ الذي كنت أشعر به من قبل. بدلاً من ذلك، أشعر بنوع من الامتلاء. امتلاء يأتي من قبول الفراغ، من احتضان العدم، من السلام مع اللا شيء.</p>
    </div>
  `;
}
