export function generateFacingTruthStory(title: string, image: string): string {
  return `
    <div class="truth-story">
      <h1>${title}</h1>
      ${image ? `<img src="${image}" alt="صورة توضيحية لقصة ${title}" class="story-image">` : ''}

      <p>الحقيقة مؤلمة. ليست مؤلمة لأنها قاسية، بل لأنها عارية. عارية من الأوهام، من الأكاذيب، من الأقنعة التي نرتديها لنخفي بها وجوهنا الحقيقية.</p>

      <p>لطالما هربت من الحقيقة. هربت إلى الأوهام، إلى الأحلام، إلى الخيالات. هربت إلى عالم صنعته بنفسي، عالم أكثر راحة، أكثر أماناً، لكنه غير حقيقي.</p>

      <p>لكن الهروب من الحقيقة له ثمن. ثمن باهظ، قاسٍ، مستمر. ثمن يدفع من السلام الداخلي، من الصدق مع الذات، من القدرة على مواجهة الواقع كما هو.</p>

      <p>وفي يوم من الأيام، سقط القناع. سقط فجأة، بلا مقدمات، بلا تحذير. سقط وكشف عن وجهي الحقيقي، عن ضعفي، عن هشاشتي، عن كل ما حاولت إخفاءه.</p>

      <p>كان السقوط مؤلماً، مخيفاً، مربكاً. شعرت بالعري، بالضعف، بالهشاشة. شعرت كأنني أقف عارياً أمام العالم، بلا حماية، بلا دروع، بلا أسلحة.</p>

      <p>في البداية، حاولت التقاط القناع، إعادته إلى وجهي، الاختباء خلفه من جديد. لكن القناع كان قد تحطم، ولم يعد صالحاً للاستخدام. لم يعد بإمكاني الهروب.</p>

      <p>وفي تلك اللحظة، واجهت خيارين: إما أن أصنع قناعاً جديداً، أو أن أواجه الحقيقة. إما أن أستمر في الهروب، أو أن أتوقف وأقف أمام مرآة نفسي.</p>

      <p>اخترت المواجهة. ليس لأنني شجاع، بل لأنني تعبت من الهروب. ليس لأنني قوي، بل لأنني تعبت من الضعف. ليس لأنني مؤمن، بل لأنني تعبت من الكذب.</p>

      <p>بدأت رحلة مواجهة الحقيقة. رحلة صعبة، مؤلمة، طويلة. رحلة تتطلب الشجاعة، الصبر، الإصرار. شجاعة النظر في المرآة، صبر تحمل ما سأرى، إصرار عدم الإغماض مرة أخرى.</p>

      <p>واجهت حقيقة أنني لست كاملاً. أنني أخطئ، أفشل، أضعف. أنني أخاف، أتردد، أتراجع. أنني إنسان، بكل ما تحمله الكلمة من معنى. إنسان ضعيف وقوي، جبان وشجاع، فاشل وناجح.</p>

      <p>واجهت حقيقة أنني لست الشخص الذي كنت أتظاهر به. أن الأقنعة التي ارتديتها طوال تلك السنوات لم تكن سوى محاولة يائسة لإرضاء الآخرين، لكسب حبهم، لتجنب رفضهم.</p>

      <p>واجهت حقيقة أن السعادة لا تأتي من الهروب، بل من المواجهة. أن الحرية لا تأتي من الاختباء، بل من الظهور. أن السلام لا يأتي من الكذب، بل من الصدق.</p>

      <p>اليوم، بعد كل تلك المعاناة، بعد كل ذلك الألم، أستطيع أن أقول بصدق: إن مواجهة الحقيقة كانت أصعب شيء فعلته في حياتي، لكنها أيضاً كانت الأكثر تحريراً.</p>

      <p>لم تعد الحقيقة تؤلمني كما كانت من قبل. لم تعد تخيفني، لم تعد تهددني. صرت أراها كما هي: مرآة تعكس حقيقة وجودي، بلا تزيين، بلا تبرير، بلا أكاذيب.</p>

      <p>وأخيراً، بعد كل تلك السنوات من الهروب، وجدت نفسي. وجدتها عارية، ضعيفة، هشة، لكنها حقيقية. وأدركت أن هذه هي النعمة الحقيقية: أن تكون كما أنت، لا كما يريدك الآخرون أن تكون.</p>
    </div>
  `;
}
